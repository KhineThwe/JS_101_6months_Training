function myFunction() {
  const x = "Bob";

  let y = x; //Declare variable-y and assign x//Bob
  y = "Tom"; //Reassign variable-y with 'Tom'
  console.log(y); //Tom

  console.log(x); //Bob: No change in the value of x
}
myFunction();
