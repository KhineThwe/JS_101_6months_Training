/*
Comparison Operator: return boolean data type for eg: true or false
1. ==  Equal 
2. ===  Equal Value and type(strict operator)
3.!=   Not equal
4.!== Not equal value and type 
5.>    Greater Than
6.<    Less Than
7.>=   Greater than or equal 
8.<=   Less than or equal
 */
let a = 5;
let b = 2;
let c = "hello";

console.log(a == 5); //true
console.log(b == "2"); // true
console.log(b === "2"); //false
console.log(c == "Hello"); //false

